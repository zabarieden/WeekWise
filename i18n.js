const SUPPORTED_LANGUAGES = ['en', 'he', 'es', 'fr', 'ar'];
const RTL_LANGUAGES = ['he', 'ar'];
const LANGUAGE_NAMES = { en: 'English', he: 'עברית', es: 'Español', fr: 'Français', ar: 'العربية' };

let currentLang = 'en';

function isRTL(lang) { return RTL_LANGUAGES.includes(lang); }

function t(key) {
    const dict = translations[currentLang] || translations.en;
    if (dict[key] !== undefined) return dict[key];
    return translations.en[key] !== undefined ? translations.en[key] : key;
}

function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    currentLang = lang;
    localStorage.setItem('weekwise_lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = isRTL(lang) ? 'rtl' : 'ltr';
    applyTranslations();
    if (typeof onLanguageChanged === 'function') onLanguageChanged();
}

function loadSavedLanguage() {
    const saved = localStorage.getItem('weekwise_lang');
    setLanguage(saved && translations[saved] ? saved : 'en');
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.getAttribute('data-i18n-title'));
    });
    const langSelect = document.getElementById('language-select');
    if (langSelect) langSelect.value = currentLang;
}

